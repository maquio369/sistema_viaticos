        REFERENCES public.clases_de_vehiculos (id_clases_de_vehiculo) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT fk_vehiculos_id_estatus FOREIGN KEY (id_estatus_de_vehiculo)
        REFERENCES public.estatus_de_vehiculos (id_estatus_de_vehiculo) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT fk_vehiculos_id_usos FOREIGN KEY (id_usos_de_vehiculo)
        REFERENCES public.usos_de_vehiculos (id_usos_de_vehiculo) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT fk_vehiculos_id_empleado FOREIGN KEY (id_empleado)
        REFERENCES public.empleados (id_empleado) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
);

