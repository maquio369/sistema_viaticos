    id_estructura_administrativa integer NOT NULL,
    numero_economico text COLLATE pg_catalog."default",
    id_marca_de_vehiculo integer NOT NULL,
    id_tipo_de_vehiculo integer NOT NULL,
    id_clase_de_vehiculo integer NOT NULL,
    modelo text COLLATE pg_catalog."default",
    placas_anteriores text COLLATE pg_catalog."default",
    placas_actuales text COLLATE pg_catalog."default",
    numero_de_motor text COLLATE pg_catalog."default",
    serie text COLLATE pg_catalog."default",
    id_estatus_de_vehiculo integer NOT NULL,
    id_usos_de_vehiculo integer NOT NULL,
    id_empleado integer,
    esta_borrado boolean NOT NULL DEFAULT false,
    CONSTRAINT vehiculos_pkey PRIMARY KEY (id_vehiculo),
    CONSTRAINT fk_vehiculos_id_marca FOREIGN KEY (id_marca_de_vehiculo)
        REFERENCES public.marcas_de_vehiculos (id_marca_de_vehiculo) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
    CONSTRAINT fk_vehiculos_id_tipo FOREIGN KEY (id_tipo_de_vehiculo)
        REFERENCES public.tipos_de_vehiculos (id_tipos_de_vehiculo) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION,
